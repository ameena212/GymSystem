           private void refillCombo() {

        cbTrainers.removeAllItems(); // Remove all items from the combo box

        // Retrieve the list of trainers from the GymSystem class
        ArrayList<Employee> trainerList = GymSystem.employeeList;
        // Loop through the trainerList and add each trainer's name to the combo box
        for (Employee employee : trainerList) {
            if (employee instanceof Trainer) {
                Trainer trainer = (Trainer) employee;
                cbTrainers.addItem(trainer);
            }
        }

    

    }

    private void refillComboMem() {

        cbMem.removeAllItems(); // Remove all items from the combo box

        // Retrieve the list of members from the GymSystem class
        ArrayList<Member> memberList = GymSystem.memberList;
        // Loop through the memberList and add each member's name to the combo box
        for (Member member : memberList) {
            // Assuming the Member class has a method named getName() to retrieve the member's name
            cbMem.addItem(member.getMembershipId() + " " + member.getFirstName());
        }

    }


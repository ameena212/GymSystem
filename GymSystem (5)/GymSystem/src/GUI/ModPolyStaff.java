    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        try {
            int id = Integer.parseInt(txtId.getText());
            boolean found = false;
            for (Member member : GymSystem.memberList) {
                if (member.getMembershipId() == id) {
                    if (member instanceof Staff) {
                        // Do something with the found student
// Handle gender selection
                        // Set the text fields with existing member information
                        txtname.setText(member.getFirstName());
                        txtlname.setText(member.getSurname());
                        txtAddress.setText(member.getAddress());
                        txtPhone.setText(Integer.toString(member.getPhone()));
                        textposition.setText(((Staff) member).getPosition());
                        txtDepartment.setText(((Staff) member).getDepartment());

                    } else {
                        JOptionPane.showMessageDialog(null, "Member found, but not a staff");
                    }
                    found = true;
                    break;
                }
            }
            if (!found) {
                JOptionPane.showMessageDialog(null, "Member not found");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "An error occurred: " + ex.getMessage());
        }
    }//GEN-LAST:event_searchActionPerformed


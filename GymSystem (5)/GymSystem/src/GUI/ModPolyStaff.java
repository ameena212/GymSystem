    private void ModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModifyActionPerformed
        // TODO add your handling code here:
        try {
            int id = Integer.parseInt(txtId.getText());
            boolean found = false;
            for (Member member : GymSystem.memberList) {
                if (member.getMembershipId() == id) {
                    if (member instanceof Staff) {
                        Staff staff = (Staff) member; // Cast Member to Student

                        String name = txtname.getText();
                        String Lname = txtlname.getText();
                        // Validate first name and surname
                        if (name.length() < 2) {
                            JOptionPane.showMessageDialog(null, "Please enter a first name with at least two letters.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
                            return; // Stop further execution if an exception occurs
                        }

                        if (Lname.length() < 2) {
                            JOptionPane.showMessageDialog(null, "Please enter a surname with at least two letters.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
                            return; // Stop further execution if an exception occurs
                        }
                        staff.setFirstName(name);
                        staff.setSurname(Lname);

                        String gender = "";
                        if (f.isSelected()) {
                            gender = "Female";
                        } else if (m.isSelected()) {
                            gender = "Male";
                        }
                        staff.setGender(gender);

                        String position = textposition.getText();
                        if (position.isEmpty()) {
                            JOptionPane.showMessageDialog(null, "Please enter a position.", "Missing Input", JOptionPane.ERROR_MESSAGE);
                            return; // Stop further execution if an exception occurs
                        }

                        if (position.length() < 3) {
                            JOptionPane.showMessageDialog(null, "Please enter a more than 3 letters.", "Missing Input", JOptionPane.ERROR_MESSAGE);
                            return; // Stop further execution if an exception occurs
                        }

                        staff.setPosition(position);

                        String department = txtDepartment.getText();
                        if (department.length() < 0 || department.isEmpty()) {
                            JOptionPane.showMessageDialog(null, "Please select a department.", "Missing Input", JOptionPane.ERROR_MESSAGE);
                            return; // Stop further execution if an exception occurs
                        }
                        staff.setDepartment(department);

                        String address;
                        try {
                            address = txtAddress.getText();
                            if (address.length() < 4) {
                                JOptionPane.showMessageDialog(null, "Please enter an Address with at least four letters.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
                                return; // Stop further execution if an exception occurs
                            }
                        } catch (NullPointerException e) {
                            // Handle the exception when `txtName` is null
                            JOptionPane.showMessageDialog(null, "Please enter an Address.", "Missing Input", JOptionPane.ERROR_MESSAGE);
                            return; // Stop further execution if an exception occurs
                        }
                        staff.setAddress(address);

                        String day = (String) jDay.getSelectedItem();
                        String month = (String) Jmonth.getSelectedItem();
                        String year = (String) Jyear.getSelectedItem();
                        if (day == "Day" || month == "Month" || year == "Year" || day.isEmpty() || month.isEmpty() || year.isEmpty()) {
                            JOptionPane.showMessageDialog(null, "Please select a valid date of birth.", "Missing Input", JOptionPane.ERROR_MESSAGE);
                            return; // Stop further execution if an exception occurs
                        }
                        String dateOfBirth = day + month + year;
                        staff.setDateOfBirth(dateOfBirth);
                        int phone;
                        try {
                            phone = Integer.parseInt(txtPhone.getText());
                        } catch (NumberFormatException e) {
                            // Handle the exception when the phone number cannot be parsed as an integer
                            JOptionPane.showMessageDialog(null, "Please enter a valid phone number.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
                            return; // Stop further execution if an exception occurs
                        }
                        staff.setPhone(phone);

                        boolean hasPt = false;

                        staff.setHasPT(hasPt);
                    }
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "An error occurred: " + ex.getMessage());
        }
        JOptionPane.showMessageDialog(null, "Member modified successfully.", "Modification Complete", JOptionPane.PLAIN_MESSAGE);

        System.out.println(memberList);


    }//GEN-LAST:event_ModifyActionPerformed


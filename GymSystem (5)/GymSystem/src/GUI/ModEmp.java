    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:

        // Get the selected Employee from the employeeList
        Employee selectedEmployee = (Employee) cbEmployee.getSelectedItem();

        if (selectedEmployee != null) {

            if (selectedEmployee instanceof Trainer) {
                Trainer selectedTrainer = (Trainer) selectedEmployee;
                // Get the first member from the selected trainer's memberTList
                selectedTrainer.getMemberTList().clear();               // Remove the selected member from the gym system's memberList

                // Remove the selected member from the selected trainer's memberTList
                // Remove the selected trainer from the gym system's employeeList
                GymSystem.employeeList.remove(selectedTrainer);
                JOptionPane.showMessageDialog(this, "Trainer has been deleted");
            } else {
                GymSystem.employeeList.remove(selectedEmployee);
                JOptionPane.showMessageDialog(this, "Employee has been deleted");

            }
        }
    }//GEN-LAST:event_DeleteActionPerformed


        Employee emp = GymSystem.employeeList.get(cbEmployee.getSelectedIndex());

        // Validate and retrieve modified information
        String modifiedFirstName = txtname.getText();
        String modifiedSurname = txtlname.getText();

        String gender;
        if (f.isSelected()) {
            gender = "Female";
        } else if (m.isSelected()) {
            gender = "Male";
        } else {
            // Gender not selected
            JOptionPane.showMessageDialog(null, "Please select a gender.", "Missing Input", JOptionPane.ERROR_MESSAGE);
            return; // Stop further execution if an exception occurs
        }
        String modifiedGender = gender;

        Double modifiedSalary = Double.valueOf(txtsalary.getText());

        if (modifiedSalary < 0) {
            throw new IllegalArgumentException("Salary cannot be negative.");

        }

        if (modifiedSalary < 0) {
            JOptionPane.showMessageDialog(null, "Please enter a correct salary.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
            return; // Stop further execution if an exception occurs
        }

        String modifiedAddress;
        try {
            modifiedAddress = txtAddress.getText();
            if (modifiedAddress.length() < 4) {
                JOptionPane.showMessageDialog(null, "Please enter an address with at least four letters.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
                return; // Stop further execution if an exception occurs
            }
        } catch (NullPointerException e) {
            // Handle the exception when `txtAddress` is null
            JOptionPane.showMessageDialog(null, "Please enter an address.", "Missing Input", JOptionPane.ERROR_MESSAGE);
            return; // Stop further execution if an exception occurs
        }

        String modifiedDay = (String) jDay.getSelectedItem();
        String modifiedMonth = (String) Jmonth.getSelectedItem();
        String modifiedYear = (String) Jyear.getSelectedItem();
        if (modifiedDay.equals("Day") || modifiedMonth.equals("Month") || modifiedYear.equals("Year") || modifiedDay.isEmpty() || modifiedMonth.isEmpty() || modifiedYear.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please select a valid date of birth.", "Missing Input", JOptionPane.ERROR_MESSAGE);
            return; // Stop further execution if an exception occurs
        }
        String modifiedDateOfBirth = modifiedDay + modifiedMonth + modifiedYear;

        int modifiedPhone;
        try {
            modifiedPhone = Integer.parseInt(txtPhone.getText());
        } catch (NumberFormatException e) {
            // Handle the exception when the phone number cannot be parsed as an integer
            JOptionPane.showMessageDialog(null, "Please enter a valid phone number.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
            return; // Stop further execution if an exception occurs
        }

        boolean modifiedIsPt = false;
        if (yes.isSelected()) {
            modifiedIsPt = true;
        } else if (no.isSelected()) {
            modifiedIsPt = false;
        } else {
            // Personal trainer selection not made
            JOptionPane.showMessageDialog(null, "Please select whether the member has a personal trainer or not.", "Missing Input", JOptionPane.ERROR_MESSAGE);
            // Stop further execution if an exception occurs
        }

        // Validate modified first name and surname
        if (modifiedFirstName.length() < 2) {
            JOptionPane.showMessageDialog(null, "Please enter a first name with at least two letters.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
            return; // Stop further execution if an exception occurs
        }

        if (modifiedSurname.length() < 2) {
            JOptionPane.showMessageDialog(null, "Please enter a surname with at least two letters.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
            return; // Stop further execution if an exception occurs
        }

        // Update member information
        emp.setFirstName(modifiedFirstName);
        emp.setSurname(modifiedSurname);
        emp.setGender(modifiedGender);
        emp.setSalary(modifiedSalary);
        emp.setDateOfBirth(modifiedDateOfBirth);
        emp.setPhone(modifiedPhone);
        emp.setIsPersonalTrainer(modifiedIsPt);
        emp.setAddress(modifiedAddress);

        // Display success message
        JOptionPane.showMessageDialog(null, "Employee modified successfully.", "Modification Complete", JOptionPane.PLAIN_MESSAGE);
        System.out.println("modified");
        System.out.println(employeeList);


